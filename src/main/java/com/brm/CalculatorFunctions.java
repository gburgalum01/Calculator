package com.brm;

public class CalculatorFunctions {

    public int add(int x, int y) {
        return x + y;
    }

    public int subtract(int x, int y) {
        return x - y;
    }

    public int multiply(int x, int y) {
        return x * y;
    }

    public int divide(int x, int y) {
        return x / y;
    }

    public double squareRoot(double x) {
        return Math.sqrt(x);
    }
}
